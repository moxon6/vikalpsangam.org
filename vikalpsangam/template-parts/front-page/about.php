    <h2>ABOUT VIKALP SANGAM</h2>
    <div class="row no-margin">
        The name 'Vikalp Sangam' is Hindi for "Alternatives' Confluence"!
    <br>
    <br>
        As the world hurtles towards greater ecological devastation, inequalities, and social conflicts, the biggest question facing us is: are there alternative ways of meeting human needs and aspirations, without trashing the earth and without leaving half of humanity behind?
    <br>
    <br>Across India (as in the rest of the world), this question is being answered by a multitude of grassroots and policy initiatives: from meeting basic needs in ecologically sensitive ways to decentralised governance and producer-consumer movements, from rethinking urban and rural spaces towards sustainability to struggles for social and economic equity.
    <br>
    <br>
        Unfortunately, documentation and public awareness of such initiatives in India is poor. 
        There are very few attempts to consolidate and present in cohesive manner, the range of these alternatives.
    <br>
    <br>
        We offer this website as one response to these gaps. 
        It hopes to feature alternatives in the full range of human endeavour as they take place in India, and through this to help build bridges amongst them, learn from each other, and together present a challenge to the mainstream system.
        This is part of a larger process in which the co-host organizations are involved, at networking alternatives in India and internationally....
